package com.example.mywidget;

public class Special {
    private int nameId;
    private int imageId;

    public Special(int nameId, int imageId){
            this.nameId = nameId;
            this.imageId = imageId;
    }

    public int getNameId() {
        return nameId;
    }


    public int getImageId() {
        return imageId;
    }
}
